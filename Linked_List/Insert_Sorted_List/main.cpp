class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
        // Code here
        Node * new_node=new Node(data);
        Node* tmp=head;
        
        while(tmp && tmp->data<data) tmp=tmp->next;
        if(tmp==head)
        {
            new_node->next=tmp;
            head=new_node;
            return head;
        }
        else if(tmp->next!=NULL)
        {
            new_node->next=tmp->next;
            
        }
        tmp->next=new_node;
        return head;
    }
};


